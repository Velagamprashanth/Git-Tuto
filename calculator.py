# My calculator program

# Ask user for input
x = float(input("Enter the first number (x): "))
y = float(input("Enter the second number (y): "))

# Perform calculations and print results
ans1 = x + y
print("Addition of x and y is", ans1)

ans2 = x - y
print("Subtraction of x and y is", ans2)

ans3 = x * y
print("Multiplication of x and y is", ans3)

ans4 = x / y
print("Division of x and y is", ans4)

ans5 = x // y
print("Floor division of x and y is", ans5)

ans6 = x % y
print("Modulus of x and y is", ans6)

ans7 = x ** y
print("Exponent of x and y is", ans7)